class QuestionDataSource {
  static const String _assetPath = 'assets/data/questions/';

  static final Map<String, List<Question>> _questionCache = {};

  static Future<List<Question>> loadQuestions(String locationId) async {
    // Check cache first
    if (_questionCache.containsKey(locationId)) {
      return _questionCache[locationId]!;
    }

    // Load from JSON
    try {
      final jsonString = await rootBundle.loadString('$_assetPath$locationId.json');
      final jsonData = json.decode(jsonString);
      final questionPack = QuestionPack.fromJson(jsonData);

      // Cache the questions
      _questionCache[locationId] = questionPack.questions;

      return questionPack.questions;
    } catch (e) {
      // If file not found, return empty list
      // In production, might want to return sample questions
      return _getSampleQuestions(locationId);
    }
  }

  static Future<Question?> getQuestion(String locationId, int stageId) async {
    final questions = await loadQuestions(locationId);
    if (stageId > 0 && stageId <= questions.length) {
      return questions[stageId - 1];
    }
    return null;
  }

  // Sample questions for when JSON files are not available
  static List<Question> _getSampleQuestions(String locationId) {
    final samples = _sampleQuestions[locationId] ?? [];
    _questionCache[locationId] = samples;
    return samples;
  }

